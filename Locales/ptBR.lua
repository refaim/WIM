WIM (WoW Instant Messenger)
|cffffffff
WIM é exatamente o que o nome diz; uma interface de mensagens instantâneas para seus sussurros no jogo. Foi especialmente projetado para não interferir com sua interface (em raids) e ao mesmo tempo oferecer a conveniência de uma janela de chat para cada usuário.

Verifique seus atalhos de teclado e procure as ações disponíveis do WIM.
|r
Comandos úteis:
/wim			|cffffffff- Janela de opções|r
/wim history	|cffffffff- Visualizador de histórico|r
/wim help		|cffffffff- (esta janela)|r

Comandos avançados:
/wim reset			|cffffffff- Redefinir todas as opções.|r
/wim reset filters	|cffffffff- Recarregar definições de filtros.|r
/wim clear history	|cffffffff- Limpar histórico.|r

WIM se integra com os seguintes addons:|cffffffff
TitanPanel|r
